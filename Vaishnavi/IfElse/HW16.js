// 16. Create a program that checks if a number entered by the user is divisible by either 2 or 3.
var number = 20;

if (number % 2 === 0 || number % 3 === 0) {
    console.log("The number is divisible by either 2 or 3.");
} else {
    console.log("The number is not divisible by either 2 or 3.");
}
