// 8. Write a program that finds the day of the week based on a number input (1 for Sunday, 2 for Monday, and so on).
const dayNumber = 3;

if (dayNumber === 1) {
    console.log("Sunday");
} else if (dayNumber === 2) {
    console.log("Monday");
} else if (dayNumber === 3) {
    console.log("Tuesday");
} else if (dayNumber === 4) {
    console.log("Wednesday");
} 
else if (dayNumber === 5) {
    console.log("Thursday");
} 
else if (dayNumber === 6) {
    console.log("Friday");
} 
else if (dayNumber === 7) {
    console.log("Saturday");
} 

